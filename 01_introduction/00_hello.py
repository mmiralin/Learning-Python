print('Tipos de datos: ')
print(type(10))                     # Int
print(type(10.5))                   # Float
print(type(True))                   # Boleano
print(type(1 + 3j))                 # Número complejo
print(type('10'))                   # String
print(type([10, 11, 12]))           # Array o Lista
print(type({'name': 'John Doe'}))   # Diccionario
print(type({10, 11, 12}))           # Set
print(type((10, 11, 12)))           # Tupla
print(type(print('Tipo NoneType')));# NoneType
